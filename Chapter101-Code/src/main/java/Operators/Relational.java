package Operators;

public class Relational {
    public static void main(String[] args) {

        int a = 10;
        int b= 20;

//        > = < <= == !=  ---> true or false

        System.out.println(a<b);
        System.out.println(10<=10);
        System.out.println(a>b);
        System.out.println('A'>='a');

        System.out.println("kumar" == "KUMAR");

        System.out.println("kumar" != "KUMAR");

    }
}
