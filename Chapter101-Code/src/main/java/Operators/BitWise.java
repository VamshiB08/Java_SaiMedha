package Operators;

public class BitWise {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        System.out.println(a & b); // bitwise and

        System.out.println(a | b); //bitwise or

        System.out.println(a ^ b); // bitwise EXOR

        System.out.println(~10);
    }
}
